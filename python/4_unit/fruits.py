# 喜欢的水果：创建一个列表，其中包含你喜欢的水果，再编写一系列独立的if语句，检查列表中是否包含特定的水果。

# （1）将该列表命名为favorite_fruits，并在其中包含三种水果。
favorite_fruits = ['apple', 'lemon', 'plum']

# （2）编写5条if语句，每条都检查某种水果是否包含在列表中，如果包含在列表中，就打印一条消息，如“我真的很喜欢香蕉！”
if 'banana' in favorite_fruits:
    print('我真的很喜欢banana')
if 'lemon' in favorite_fruits:
    print('我真的很喜欢lemon')
if 'jujube' in favorite_fruits:
    print('我真的很喜欢jujube')
if 'plum' in favorite_fruits:
    print('我真的很喜欢plum')
if 'apple' in favorite_fruits:
    print('我真的很喜欢apple')

